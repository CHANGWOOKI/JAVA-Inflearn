package operator;

public class Assign1 {
    public static void main(String[] args) {
        // 대입 연산자 (대입과 연산을 한번에) 줄여서 쓰려고 한건다
        int a = 5;
        a += 3; // a = a + 3   (8)
        System.out.println(a);
        a *= 3; // a = a * 3   8 * 3 (24)
        System.out.println(a);
        a -= 4; // a = a - 2   24 - 4 (20)
        System.out.println(a);


    }
}
