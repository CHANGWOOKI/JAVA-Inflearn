package variable;

public class Var5 {
    public static void main(String[] args) {
        // 1. 변수 선언과 초기화를 따로
        int a;  // 변수 선언
        a = 10; // 변수 초기화
        System.out.println(a);

        // 2. 변수 선언과 초기화 같이
        int b = 20;
        System.out.println(b);

        // 3. 여러 개 변수 선언과 초기화 같이
        int c = 30, d = 40;
        System.out.println(c);
        System.out.println(d);
    }
}
