package variable;

public class Var7 {
    public static void main(String[] args) {
        // 변수 타입
        // 타입, 형식, 형이라도 부른다
        // 타입에 맞지 않은 값을 저장할시 컴파일 오류나옴
        // literal(리터널) : 개발자가 변수에 직접 입력한 고정한 값(입력받지 않고 개발자가 직접 넣은 값)
        int a = 100;    // 정수
        double b = 10.5;    // 실수
        boolean c = true; // 불리언(boolean) true, false 입력 가능
        char d = 'B';   // 문자 하나
        String e = "Hello Java";    // 문자열, 문자열을 다루기 위한 타입

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
